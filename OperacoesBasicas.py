# Define valores. Ponto Flutuante
c = 5.0
d = 2.0

# Operações básicas
soma = c + d
subtracao = c - d
multiplicacao = c * d
divisao = c / d

print('Resultado das operações entre c e d')
print("c e d definidos como ponto flutuante")
print("Resultados são em ponto flutuante")
print(f"Soma =  {soma}")
print(f"Subtração = {subtracao}")
print(f"Multiplicacao = {multiplicacao}")
print(f"Divisao = {divisao}")

