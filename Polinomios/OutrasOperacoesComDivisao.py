# Define valores. Ponto flutuante
a = 10.0
b = 3.0
# Operações

# Resultado é somente a parte inteira da divisão
divisao_inteira = a // b
#Resultado é o resto da divisão
resto_divisao = a % b

print("Resultado das operações")
print(f"a / b (Parte inteira) = {divisao_inteira}")
print(f"Resto de a / b =  {resto_divisao}")
